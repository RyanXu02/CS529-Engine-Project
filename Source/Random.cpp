#pragma region Public Functions
	void Random::Initialize() {
		std::random_device rd;
		generator = std::mt19937(rd());
	}

	int Random::Range(int rangeMin, int RangeMax) {
		std::uniform_int_distribution<> distribution(rangeMin, RangeMax);
		return distribution(generator);
	}

	float Random::Range(float rangeMin, float RangeMax) {
		std::uniform_real_distribution<> distribution(rangeMin, RangeMax);
		return static_cast<float>(distribution(generator));
	}
